// also keep ans = min(ans, v_r.ff - v_l.ff);
ll N = 0, l = 0, r = 0, ans = oo; 
 vector<ll> notes(n, 0);
while(r < 6*n){
        while(r < 6*n && N != n){
            notes[v[r].ss]++;
            if(notes[v[r].ss] == 1) N++;
            r++;
        }
        while(l < r && N == n){
            notes[v[l].ss]--;
            if(notes[v[l].ss] == 0) N--;
            l++;
        }        
        ans = min(ans, v[r-1].first - v[l-1].first);
}
