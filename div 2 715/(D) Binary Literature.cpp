 string mix(string s, string t, char c) {
    int n = s.size()/2;
    vector<int> v1 = {-1}, v2 = {-1};
    for(int i = 0; i < 2 * n; i++) {
        if(s[i] == c)
            v1.push_back(i);
        if(t[i] == c)
            v2.push_back(i);
    }
    string ans;
    for(int i = 0; i < n; i++) {
        for(int j = v1[i] + 1; j < v1[i + 1]; j++)
            ans += s[j];
        for(int j = v2[i] + 1; j < v2[i + 1]; j++)
            ans += t[j];
        ans += c;
    }
    for(int j = v1[n] + 1; j < 2 * n; j++)
        ans += s[j];
    for(int j = v2[n] + 1; j < 2 * n; j++)
        ans += t[j];
    return ans;
}
 

